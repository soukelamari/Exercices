package variable;

public class Cours {

	public static void main(String[] args) {

		// Pour declarer des variables

		byte x1 = -128; // -128 => 127
		short x2 = 5000;// -32 768 => 32 767
		int x3 = 100500;
		long x4 = 546645454;

		float x5 = 3.5f;// 1.5 cm *** 3.5f => f => float
		double x6 = 546645454.6;// 1.5 cm **** 1.5d => d => double (pas necessaire)

		char s = 'A';// caractere

		String chain = "C'est une chaine";

		// Affichage dans la console

		System.out.println(x1);
		System.out.println(x2);
		System.out.println(x3);
		System.out.println(x4);
		System.out.println(x5);
		System.out.println(x6);
		System.out.println(s);
		System.out.println(chain);
		
		System.out.print("Ziad");
		
		// syso => ctrl + espace => Entrer
		System.out.print("Souk");
		
	}
}
